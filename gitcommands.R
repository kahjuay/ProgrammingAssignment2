# git commands

# Do local clone
git clone https://github.com/kahjuay/ProgrammingAssignment2

# Check which branch you are on
git branch

# Check remote git repo
git remote -v

# Check git repo status
git status

# Add changed file
git add gitcommands.R

# Commit changes (type comments in notepad that pops up)
git commit -a

# Move changes to git repo
git push